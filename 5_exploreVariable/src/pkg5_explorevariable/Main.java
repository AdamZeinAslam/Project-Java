package pkg5_explorevariable;


public class Main {

  
    public static void main(String[] args) {
        //integer
        int umur = 19;
        System.out.println("Umur = " + umur);
        
       //float
       float berat = (float) 57.5;
       System.out.println("Berat = " + berat);
       
       //double folating
       double tinggi = 165.333;
       System.out.println("Tinggi = " + tinggi);
       
       //string
       String nama = "Adam Zein Aslam";
       System.out.println("Nama = " + nama);
       
       //Karakter (char)
       char jenisKelamin = 'L';
       System.out.println(jenisKelamin);
       
       //boolean
       boolean ganteng = true;
       System.out.println("Ganteng = " + ganteng);
       
        
    }
    
}
