
package pkg16_increment;


public class Main {

    
    public static void main(String[] args) {
      /*
        increment 
        post incremet = a++
        pre increment = ++a
        */ 
      
      int a;
      int i = 1;
      a = ++i;
      /*
      a = i, -> i = i + 1;
      */
        System.out.println(a);
    }
    
}
